function passMask(pass){
  let str = "";
  for (let index = 0; index < pass.length; index++) {
    str += "*";
  }
  return str;
}

function copyText(text){
  navigator.clipboard.writeText(text).then(() => {
    const notification = document.getElementById("copy-notification");
    notification.style.display = "block";

    setTimeout(() => {
      notification.style.display = "none";
    },2500);
  }).catch((err) => {
     const notification = document.getElementById("copy-notification");
     notification.style.background = "#ff3434";
     notification.innerText = "Failed to Copy";
  })
}

function deletePassword(index) {
  let passwords = localStorage.getItem("passwords");
  if (passwords) {
    let arr = JSON.parse(passwords);
    arr.splice(index, 1); // Remove item at index
    localStorage.setItem("passwords", JSON.stringify(arr));
    showPasswords(); // Refresh table
    alert("Password deleted!");
  }
}

